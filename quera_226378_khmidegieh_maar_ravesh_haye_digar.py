    #قوانین حرکت
    if move == "L":
        i, j = i - 1, j + 1
    elif move == "F":
        i, j = i, j + 1
    elif move == "R":
        i, j = i + 1, j + 1

    # بررسی اینکه داخل جدول است یا خیر
