# # حال نوروز بلیط سفر خود را از بانکت خریده‌ و می‌داند که دقیقا NN روز به سفر می‌رود، اما به‌دلیل افسردگی از قرنطینه، نمره خود را حساب نمی‌کند. به او کمک کنید و بگویید چه نمره‌ای در این درس می‌گیرد.
