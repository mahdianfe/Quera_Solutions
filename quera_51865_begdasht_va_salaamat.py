# # وروز که به دلیل قرنطینه کم‌حوصله شده، می‌خواهد به سفری تفریحی برود. (او با استفاده از اپلیکیشن بانکت بلیطی برای سفر تهیه کرده است). وی با استاد خود، آقا فیروز صحبت می‌کند که از او اجازه بگیرد تا بتواند در کلاس‌های آنلاینش شرکت نکند.

